package nebulosa.constants

/**
 * The constant PI.
 */
const val PI = Math.PI

/**
 * The constants Tau (2 * PI).
 */
const val TAU = 2 * PI

/**
 * The half of PI.
 */
const val PIOVERTWO = PI / 2

/**
 * Radians to degrees.
 */
const val RAD2DEG = 180.0 / PI

/**
 * Degrees to radians.
 */
const val DEG2RAD = PI / 180.0

/**
 * Angular velocity in radians/s.
 */
const val ANGULAR_VELOCITY = 7.2921150E-5

/**
 * Arcseconds in a full circle.
 */
const val TURNAS = 1296000.0
